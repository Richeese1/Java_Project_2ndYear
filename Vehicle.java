
public class Vehicle {
	
	private String name;
	int age;
	

	public void setName(String newName) {		
		this.name = newName;
		
	}
	
	public String getName() {
		
		return name;
		
	}
	public void setAge(int newAge) {
		
		this.age = newAge;	
		
	}
	
	public int getAge() {
		
		return age;
		
	}

}
